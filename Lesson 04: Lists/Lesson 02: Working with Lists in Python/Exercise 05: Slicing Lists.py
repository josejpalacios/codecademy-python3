# Creating a selection from a list is called slicing.

# We can do this using the following syntax: letters[start:end].

# start is the index of the first element that we want to include in our selection.

# end is the index of one more than the last index that we want to include.

suitcase = ['shirt', 'shirt', 'pants', 'pants', 'pajamas', 'books']

beginning = suitcase[0:4]
print(beginning)

# List with two middle items from suitcase
middle = suitcase[2:4]
